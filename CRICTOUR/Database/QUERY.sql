SELECT
    (P.FIRST_NAME || ' ' || P.LAST_NAME) AS FULL_NAME,
    P.NATIONALITY,
    EXTRACT(YEAR FROM CURRENT_DATE) - EXTRACT(YEAR FROM C.START_DATE_OF_CAREER) AS COACHING_TIME,
    P.IMAGE,
    P.PERSONID,
    (
        SELECT TEAM_NAME
        FROM TEAM T
        WHERE T.TEAM_ID = C.TEAM_ID
    ) AS TEAM_NAME
FROM COACH C
JOIN PERSON P ON C.PERSONID = P.PERSONID;



